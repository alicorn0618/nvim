vim.g.mapleader = " "
vim.o.background = "light"

-- 忽略大小写搜索
vim.opt.ignorecase = true

-- 设置 Tab 宽度为 4 个空格
vim.opt.tabstop = 4

-- 开启光标所在行的高亮
vim.opt.cursorline = true

-- 设置软 Tab 宽度为 4 个空格
vim.opt.softtabstop = 4

-- 设置缩进宽度为 4 个空格
vim.opt.shiftwidth = 4

-- 启用缩进对齐到最接近的 `shiftwidth`
vim.opt.shiftround = true

